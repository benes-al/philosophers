#include "philo.h"

int	atoi_mod(const char *argv)
{
	int i;
	int n;
	
	i = 0;
	n = 0;
	while (argv[i])
	{
		if (argv[i] < '0' || argv[i] > '9')
			return (-1);
		n = (n * 10) + (argv[i] - '0');		
		i++;
	}
	return (n);	
}